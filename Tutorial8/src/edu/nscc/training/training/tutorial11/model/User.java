package edu.nscc.training.training.tutorial11.model;

public class User {
	public int id = 7;
	public int age = 25;
	public String name = "Jack";
	
	
	public int getId(){
		return id;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public void setId(int id) {
		this.id = id;
	}
	
	
}
